    private void enviarMensajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarMensajeActionPerformed
        String accion = "";
        if(!this.mensaje.getText().equals("")){
            if(((String)contactosMandarMensaje.getSelectedItem()).equals("Todos")){
                accion = "Publico#"+mensaje.getText();
            }else{
                String nombreContacto = (String)contactosMandarMensaje.getSelectedItem();
                accion = "Privado#"+nombreContacto+"#"+mensaje.getText();
            }
            this.c.enviarComando(accion);
        }

        mensaje.setText("");
    }//GEN-LAST:event_enviarMensajeActionPerformed

