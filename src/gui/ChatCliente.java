    public void setConectados(ArrayList<String> conectados){
        this.contactosConectados.setText("");
        this.contactosMandarMensaje.removeAllItems();
        this.contactosMandarMensaje.addItem("Todos");
        
        if(conectados.isEmpty()){
            this.contactosConectados.setText("Sin contactos en línea. \n Espera, por favor :)");
        }else{
            for(String usuario : conectados){
                this.contactosConectados.setText(this.contactosConectados.getText()+usuario+"\n");
                this.contactosMandarMensaje.addItem(new String(usuario));
            }
        }
    }
    
    public void addUsuario(String s){
        if(this.contactosConectados.getText().equals("Sin contactos en línea. \n Espera, por favor :)")){
            this.contactosConectados.setText(s+"\n");
        }else{
            this.contactosConectados.setText(this.contactosConectados.getText()+s+"\n");
        }
        this.contactosMandarMensaje.addItem(new String(s));
    }
    
    public void quitarConectado(String s){
        ArrayList<String> nuevaLista = new ArrayList<>();
        int pos = 0;
        String aux = "";
        
        while(pos<this.contactosConectados.getText().length()){
            if(this.contactosConectados.getText().charAt(pos)!='\n'){
                aux += this.contactosConectados.getText().charAt(pos);
            }else{
                nuevaLista.add(aux);
                aux = "";
            }
            pos++;
        }
        
        if(nuevaLista.isEmpty()){
            this.contactosConectados.setText("Sin contactos en línea. \n Espera, por favor :)");
        }else{
            for(String saux : nuevaLista){
                if(saux.equals(s)){
                    nuevaLista.remove(saux);
                    break;
                }
            }
            if(nuevaLista.isEmpty()){
                this.contactosConectados.setText("Sin contactos en línea. \n Espera, por favor :)");
            }else{
                for(String saux : nuevaLista){
                    this.addUsuario(saux);
                }
                //setConectados(nuevaLista);
            }
        }
        
        int posU = -1;
        
        for(int i=0;i<this.contactosMandarMensaje.getItemCount();i++){
            if(((String)this.contactosMandarMensaje.getItemAt(i)).equals(s))
                posU = i;
        }
        this.contactosMandarMensaje.removeItemAt(posU);
    }
   
